"""Helper functions to continue computation when result is not None."""

from ..compat import *

class maybe(object):
    def __init__(self, value):
        self.value = value
    def __getattr__(self, attr):
        if self.value is None:
            def f(*args, **kwargs):
                return None
            return f
        return getattr(self.value, attr)
